from typing import List


class Solution:
    def sampleStats(self, count: List[int]) -> List[float]:
        min_num = 256
        max_num = -1
        nonzeros = 0
        total = 0
        mode = 0
        max_cnt, mode = 0, 0
        for i, c in enumerate(count):
            if c == 0:
                continue
            if i < min_num:
                min_num = i
            if i > max_num:
                max_num = i
            if c > max_cnt:
                max_cnt, mode = c, i
            nonzeros += c
            total += i * c
        mean = total / nonzeros
        median = self.get_median(count, nonzeros)
        res = [float(min_num), float(max_num), mean, median, float(mode)]
        return res

    def get_median(self, count, nonzeros):
        idx = []
        if nonzeros % 2 == 1:
            idx.append((nonzeros - 1) // 2 + 1)
        else:
            h = nonzeros // 2
            idx.append(h - 1)
            idx.append(h)
        nums = []
        cur = 0
        for i, c in enumerate(count):
            while len(nums) < len(idx) and cur + c > idx[len(nums)]:
                nums.append(i)
            cur += c
        return sum(nums) / len(nums)


